package inhatc.capstone.market.findMarket;

public class FindMarketVO {
	private int mk_number;
	private int seller_number;
	private String mk_name;
	private String mk_address;
	private String mk_tel;
	private boolean mk_state;
	private boolean mk_delivery;
	private String mk_intro;
	
	public int getMk_number() {
		return mk_number;
	}
	public void setMk_number(int mk_number) {
		this.mk_number = mk_number;
	}
	public int getSeller_number() {
		return seller_number;
	}
	public void setSeller_number(int seller_number) {
		this.seller_number = seller_number;
	}
	public String getMk_name() {
		return mk_name;
	}
	public void setMk_name(String mk_name) {
		this.mk_name = mk_name;
	}
	public String getMk_address() {
		return mk_address;
	}
	public void setMk_address(String mk_address) {
		this.mk_address = mk_address;
	}
	public String getMk_tel() {
		return mk_tel;
	}
	public void setMk_tel(String mk_tel) {
		this.mk_tel = mk_tel;
	}
	public boolean getMk_state() {
		return mk_state;
	}
	public void setMk_state(boolean mk_state) {
		this.mk_state = mk_state;
	}
	public boolean getMk_delivery() {
		return mk_delivery;
	}
	public void setMk_delivery(boolean mk_delivery) {
		this.mk_delivery = mk_delivery;
	}
	public String getMk_intro() {
		return mk_intro;
	}
	public void setMk_intro(String mk_intro) {
		this.mk_intro = mk_intro;
	}
}
