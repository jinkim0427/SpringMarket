package inhatc.capstone.market.shopping;

public class ShoppingVO {
	private int pd_number;
	private int mk_number;
	private int pd_category;
	private String pd_name;
	private int pd_weight;
	private int pd_amount;
	private int pd_price;
	private String pd_img;
	
	public int getPd_number() {
		return pd_number;
	}
	public void setPd_number(int pd_number) {
		this.pd_number = pd_number;
	}
	public int getMk_number() {
		return mk_number;
	}
	public void setMk_number(int mk_number) {
		this.mk_number = mk_number;
	}
	public int getPd_category() {
		return pd_category;
	}
	public void setPd_category(int pd_category) {
		this.pd_category = pd_category;
	}
	public String getPd_name() {
		return pd_name;
	}
	public void setPd_name(String pd_name) {
		this.pd_name = pd_name;
	}
	public int getPd_weight() {
		return pd_weight;
	}
	public void setPd_weight(int pd_weight) {
		this.pd_weight = pd_weight;
	}
	public int getPd_amount() {
		return pd_amount;
	}
	public void setPd_amount(int pd_amount) {
		this.pd_amount = pd_amount;
	}
	public int getPd_price() {
		return pd_price;
	}
	public void setPd_price(int pd_price) {
		this.pd_price = pd_price;
	}
	public String getPd_img() {
		return pd_img;
	}
	public void setPd_img(String pd_img) {
		this.pd_img = pd_img;
	}
	
}
