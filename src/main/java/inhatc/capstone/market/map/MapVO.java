public class MapVO {
	private int mp_number;
	private int mk_number;
	private String mp_address;
	private String mp_name;
	private double mp_lat;
	private double mp_lon;
	
	public int getMp_number() {
		return mp_number;
	}
	public void setMp_number(int mp_number) {
		this.mp_number = mp_number;
	}
	public int getMk_number() {
		return mk_number;
	}
	public void setMk_number(int mk_number) {
		this.mk_number = mk_number;
	}
	public String getMp_address() {
		return mp_address;
	}
	public void setMp_address(String mp_address) {
		this.mp_address = mp_address;
	}
	public String getMp_name() {
		return mp_name;
	}
	public void setMp_name(String mp_name) {
		this.mp_name = mp_name;
	}
	public double getMp_lat() {
		return mp_lat;
	}
	public void setMp_lat(double mp_lat) {
		this.mp_lat = mp_lat;
	}
	public double getMp_lon() {
		return mp_lon;
	}
	public void setMp_lon(double mp_lon) {
		this.mp_lon = mp_lon;
	}
	
}
